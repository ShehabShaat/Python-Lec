

# Equal + Not Equal
print(100==100)
print(100==200)
print(100==100.00)
print("#"*50)
print(100!=100)
print(100!=200)
print(100!=100.00)

print("="*50)

# Less Than + Grater Than
print(100>100)
print(100>200)
print(100>100.00)
print("#"*50)
print(100<100)
print(100<200)
print(100<100.00)

print("="*50)

#  Grater Than Or Equal , Less Than Or Equal
print(100>=100)
print(100>=200)
print(100>=100.00)
print("#"*50)
print(100<=100)
print(100<=200)
print(100<=100.00)